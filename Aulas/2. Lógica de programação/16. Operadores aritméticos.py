# Operadores Básicos: 
print('Multiplicação (*)', 10 * 10)
print('Adição (+)', 10 + 10)
print('Subtração (-)', 10 - 5)
print('Divisão (/)', 10 / 2)

# Operadores adicionais
print('Potenciação (**)', 10 ** 3)  # Retorna uma potência do primeiro valor com o segundo (1²)
print('Divisão Inteira (//)', 10 // 3)  # Retorna um valor arredondado inteiro
print('Resto da divisão (%)', 10 % 3)  # Retorna o resto da divisão (módulo), caso tenha
